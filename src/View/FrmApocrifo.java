    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // BOTON PARA ELIMINAR UN REGISTRO DE APOCRIFO.
        
        int opcion = JOptionPane.showConfirmDialog(null, "ESTAS SEGURO QUE QUIERES ELIMANR UN REGISTRO", "CONFIRMAR", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_NO_OPTION) {
            if (txtmatricula.getText().toString().equals("") || txtmatricula.getText() == null) {
                Icon m = new ImageIcon(getClass().getResource("/Image/unitec.jpeg"));
                JOptionPane.showMessageDialog(null, "NO SE PUEDE ELIMINAR SI NO HAY MATRICULA");;
            } else {
                DaoApocrifo da = new DaoApocrifo();
                int resultado = da.eliminar(txtmatricula.getText());
                if (!da.isIsError() || resultado != 0) {
                    Icon m = new ImageIcon(getClass().getResource("/Image/unitec.jpeg"));
                    JOptionPane.showMessageDialog(null, "SE ELIMINO CORRECTAMENTE");
                } else {
                    Icon m = new ImageIcon(getClass().getResource("/Image/unitec.jpeg"));
                    JOptionPane.showMessageDialog(null, "NO SE PUDO ELIMAR EL REGISTRO, error: " + da.getError());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "NO SE REALIZO NINGUNA MODIFICACIÓN");
        }
        DaoApocrifo da = new DaoApocrifo();
        modeloapocrifo.getDataVector().removeAllElements();
        tabla_apocrifo.updateUI();
        da.clean();
        da.buscartabla(tabla_apocrifo);
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

