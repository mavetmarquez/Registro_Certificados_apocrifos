    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        //BOTON PARA GUARDAR UN REGISTRO APOCRIFO
        
        Apocrifo a = new Apocrifo();
        a.setMatricula(txtmatricula.getText());
        a.setNombre(txtnombre.getText());
        a.setDocumento(cboDocumento.getSelectedItem().toString());
        a.setObservaciones(txtObservaciones.getText());
        
        Date fecha_1 = fecha_escaneo.getDate();
        SimpleDateFormat formato1 = new SimpleDateFormat("dd/MM/yyyy");
        String fecha1 = formato1.format(fecha_1);
        a.setFecha_escaneo(fecha1);
        
        Date fecha_2 = fecha_bloqueo.getDate();
        SimpleDateFormat formato2 = new SimpleDateFormat ("dd/MM/yyyy");
        String fecha2 = formato2.format(fecha_2);
        a.setFecha_bloqueo(fecha2);
        
        Date fecha_3 = fecha_revision.getDate();
        SimpleDateFormat formato3 =  new SimpleDateFormat("dd/MM/yyyy");
        String fecha3 = formato3.format(fecha_3);
        a.setFecha_revision(fecha3);
        
        a.setPredictamen(cboPredictamen.getSelectedItem().toString());
        a.setOficio(cboOficio.getSelectedItem().toString());
        
        int opcion = JOptionPane.showConfirmDialog(null, "ESTAS SEGURO QUE QUIERES GUARDAR EL REGISTRO", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (opcion == JOptionPane.YES_NO_OPTION ) {

                DaoApocrifo da = new DaoApocrifo();
                int res = da.insertar(a);
                if (!da.isIsError() && res != 0) {

                    JOptionPane.showMessageDialog(null, "SE AGREGO CORRECTAMENTE. ");
                } else {

                    JOptionPane.showMessageDialog(null, "NO SE PUDO AGREGAR, error: " + da.getError());
                }

            } else {
                JOptionPane.showMessageDialog(null, "NO SE REALIZO NINGUNA MODIFICACIÓN");
            }
            
            modeloapocrifo.getDataVector().removeAllElements();
            tabla_apocrifo.updateUI();
            
            DaoApocrifo da = new DaoApocrifo();
            da.clean();
            da.buscartabla(tabla_apocrifo);
        
    }//GEN-LAST:event_BtnGuardarActionPerformed

